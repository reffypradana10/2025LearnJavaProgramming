package Bagian1;

public class KonversiNumber {
    public static void main(String[] args) {

        // Konversi Otomatis
        byte iniByte = 30;
        short iniShort = iniByte;
        int intInt = iniShort;
        System.out.println(intInt);

        // Konversi Kebalikan
        double iniDouble = 100.5;
        int iniInt2 = (int) iniDouble;

        System.out.println(iniInt2);
    }
}
